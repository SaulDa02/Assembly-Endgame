export function sortWords(words) {
	const sorted = words.sort()
	let listToString = "["

	for (let i = 0; i < sorted.length; i++) {
		if (i !== sorted.length - 1) {
			listToString = `${listToString} "${sorted[i]}",\n`;
		} else {
			listToString = `${listToString} "${sorted[i]}\n"`;
		}
	}

	listToString = listToString + "]"
	console.log(listToString)
}

